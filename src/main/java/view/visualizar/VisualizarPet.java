    private void btnBuscarPetsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBuscarPetsMouseClicked
        ArrayList<Pet> pets = petDAO.selecionar();
        DefaultTableModel model = (DefaultTableModel) tbDadosPets.getModel();
        model.setNumRows(0);

        for (int i = 0; i < pets.size(); i++) {
            model.addRow(new String[]{
                String.valueOf(pets.get(i).getCodPet()),
                pets.get(i).getNomePet(),
                pets.get(i).getRacaPet(),
                pets.get(i).getEspeciePet(),
                String.valueOf(pets.get(i).getDataNascPet()),
                pets.get(i).getSexoPet(),
                String.valueOf(pets.get(i).getTutor())});
        }
    }//GEN-LAST:event_btnBuscarPetsMouseClicked

    private void btnEditarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEditarMouseClicked
        int linhaSelecionada = tbDadosPets.getSelectedRow();

        if (linhaSelecionada != -1) {
            Object valor = tbDadosPets.getValueAt(linhaSelecionada, 0);
            int idPet = Integer.parseInt((String) valor);

            Pet petSelecionado = petDAO.selecionarPorCodigo(idPet);

            if (petSelecionado != null) {
                AlterarPet alterarPet = new AlterarPet(petSelecionado);
                this.dispose();
                alterarPet.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "Pet n√£o encontrado.");
            }

        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma linha para editar.");
        }
    }//GEN-LAST:event_btnEditarMouseClicked

    private void btnExcluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnExcluirMouseClicked
        int linhaSelecionada = tbDadosPets.getSelectedRow();

        if (linhaSelecionada != -1) {
            int idPet = Integer.parseInt((String) tbDadosPets.getValueAt(linhaSelecionada, 0));

            petDAO.excluir(idPet);
            JOptionPane.showMessageDialog(null, "Pet exclu√≠do com sucesso!");
            //            atualizarTabela();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um pet para excluir.");
        }
    }//GEN-LAST:event_btnExcluirMouseClicked


