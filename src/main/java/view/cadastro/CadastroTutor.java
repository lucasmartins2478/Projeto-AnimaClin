    private void btnSalvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSalvarMouseClicked
        // TODO add your handling code here:
        TutorDAO tutorDAO = new TutorDAO();

        String nomeTutor = this.txtNomeTutor.getText();
        String cpfTutor = this.txtCpfTutor.getText();
        String emailTutor = this.txtEmailTutor.getText();
        String telefoneTutor = this.txtTelTutor.getText();
        String enderecoTutor = this.txtEnderecoTutor.getText();

        Tutor newTutor = new Tutor(nomeTutor, cpfTutor, emailTutor, telefoneTutor, enderecoTutor);
        tutorDAO.inserir(newTutor);

        if (txtNomeTutor.getText().equals("") || txtCpfTutor.getText().equals("") || txtEmailTutor.getText().equals("") || txtTelTutor.getText().equals("") || txtEnderecoTutor.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos!");
        } else {
            JOptionPane.showMessageDialog(null, "Tutor cadastrado com sucesso!");
            this.dispose();
//            FormPrincipal f = new FormPrincipal();
//            f.setVisible(true);
        }
    }//GEN-LAST:event_btnSalvarMouseClicked

